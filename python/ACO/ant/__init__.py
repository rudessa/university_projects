
class Ant:
    def __init__(self, alpha=1, beta=1):
        """
        Инициализация муравья с параметрами для феромонов и эвристической информации.
        
        :param alpha: Параметр влияния феромонов (по умолчанию 1).
        :param beta: Параметр влияния эвристической информации (по умолчанию 1).
        """
        self.alpha = alpha  # Параметр феромонов
        self.beta = beta    # Параметр эвристической информации
        self.route = []     # Маршрут муравья
        self.visited = set()  # Множество посещенных узлов

    def reset(self, start_node):
        """
        Сбрасывает муравья для новой итерации, начиная с указанного начального узла.
        
        :param start_node: Узел, с которого начинает муравей.
        """
        self.route = [start_node]  # Инициализируем маршрут начальным узлом
        self.visited = {start_node}  # Инициализируем множество посещенных узлов
